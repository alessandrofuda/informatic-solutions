<h2 id="videocitofoni-wireless-senza-fili">I videocitofoni wireless (senza fili)</h2>
<p>I <strong>videocitofoni wireless</strong>, di fatto, superano la frontiera dei classici impianti citofonici per sfociare nella domotica e nell&#8217;ultima frontiera della tecnologia: sfruttando la rete web, ad esempio, si può rispondere al videocitofono di casa anche se si è in vacanza e, da qui, si possono governare e controllare apparecchi di casa in tempo reale: camere, videocamere, tapparelle, riscaldamento, gas, elettricità, antifurti, ecc&#8230;</p>
<p>La tecnologia wireless, di fatto, offre oggi infinite possibilità che vanno ben oltre al semplice citofono o videocitofono.</p>
<p>Ecco i principali <strong>vantaggi dei video citofoni wireless</strong>.</p>
<ul>
<li>Possono essere installati ovunque: non avendo bisogno di cavi passacavi canaline, ecc&#8230;  possono comodamente essere utilizzati anche in edifici d&#8217;epoca senza alcun lavoro di muratura.</li>
<li>Possono essere installati molto più velocemente rispetto agli impianti cablati (cioè <em>&#8220;con fili&#8221;</em>).</li>
<li>Possono essere installati molto più economicamente, visto che non c&#8217;è bisogno del muratore nè di alcun lavoro di muratura.</li>
<li>Se è già presente un attacco elettrico, basta quello (uno per la parte esterna di &#8220;chiamata&#8221; ed uno per quella interna &#8220;di risposta&#8221;).</li>
<li>Se non c&#8217;è una presa elettrica esterna è possibile oggi addirittura realizzare un piccolo punto di allaccio elettrico con un pannello fotovoltaico ed una batteria.</li>
<li>Una volta che si sono installati i due blocchi principali (quello di chiamata e quello di risposta) è possibile aggiungere altri punti di risposta facilmente in tutte le stanza della casa e collegarlo anche al proprio smartphone.</li>
<li>L&#8217;impianto è facilmente scalabile e con ogni presa di casa si può creare una nuova postazione di risposta.</li>
<li>La tecnologia è facilmente integrabile con altre funzioni già presenti nella casa. E&#8217; possibile, ad esempio, aprendo il cancello, accendere una luce o una video-camera.</li>
</ul>
