<h2 id="cosa-e-un-video-citofono">Cosa è un video citofono?</h2>
<p>In due parole: è un apparecchio che, come un normale citofono, consente di comunicare con chi si trova all&#8217;esterno. Ma, a differenza di un semplice citofono, utilizza una telecamera per mettere in comunicazione audio-video i due interlocutori in tempo reale, come una piccola webcam. Il video-citofono però, più che un &#8220;apparecchio&#8221;, è un <em>impianto</em>: un'<em>installazione</em>, che può essere anche Wireless (ovvero "senza fili").</p>
<p>Col videocitofono si può parlare e <em>vedere</em> in tempo reale con chi si sta parlando. Come vedremo, in effetti, i video citofoni non sono utilizzati solo negli ingressi degli edifici, ma possono essere usati anche in altre situazioni, anche su lunghe distanze, sfruttando la rete web e internet.</p>
<p>Come scegliere un video-citofono? Cosa valutare? Quanto può costare l&#8217;installazione? E quali sono le migliori marche?</p>
<p>Bene, in questi casi, ciò che è utile sapere non è tanto <em>cosa</em> sono i video citofoni, ma <em>quali</em> sono le tipologie, come li posso installare e quali sono i <strong><em>prezzi</em></strong>.</p>
<p>Ma prima: un preambolo sui costi, e sulle agevolazioni che dobbiamo conoscere per sfruttare, in questo momento, la possibilità di ammodernare il proprio impianto e migliorare la sicurezza della propria abitazione e dell'edificio in cui vivo.</p>




<h2 id="detrazioni-fiscali-50-per-acquisto-installazione-videocitofoni" class="collapsible">
<a class="btn left" type="button" data-toggle="collapse" data-target="#detrazioni">Le detrazioni fiscali 50% sull&#8217;acquisto e l&#8217;installazione dei videocitofoni <span class="caret"></span></a>
</h2>
<div id="detrazioni" class="collapse">

<figure class="figure pull-left">
    <img src="{{ url('images/videocitofono-300x300.jpg') }}" class="img-thumbnail" title="videocitofono" alt="videocitofono" width="300" height="300" />
    <figcaption class="figure-caption">I videocitofoni, come i sistemi di allarme, sono detraibili al 50% dal reddito Irpef</figcaption>
</figure>

<p>Sui costi, diciamo subito una cosa: chi installa un videocitofono a casa può beneficiare della <strong>detrazione fiscale</strong> riservata alle ristrutturazioni edilizie: anche solo sostituire il vecchio citofono è infatti equiparabile ad una mini-ristrutturazione di casa che, come tale, gode dello sgravio fiscale Irpef sul 50% dei costi sostenuti.</p>
<p><strong>Come funziona la detrazioni fiscale Irpef 50%?</strong></p>
<p>Se spendo 400 euro (iva inclusa), mi vengono restituiti in 10 anni 200 euro, cioè:  <em>&#8220;il 50% dei costi sostenuti&#8221;</em>.<br />
Queste 200 euro <strong>non</strong> vengono riconosciute <em>tutte e subito</em>, ma vengono &#8220;restituite&#8221; in 10 anni, cioè: 20 euro l&#8217;anno per 10 anni. Come avviene questa &#8220;restituzione&#8221;? Di fatto nessuno restituisce niente, ma avviene una sorta di &#8220;compensazione&#8221; con le tasse. La &#8220;compensazione&#8221; avviene sotto forma, appunto, di <em>detrazione fiscale</em> Irpef. Cioè: per 10 anni ogni anno verrà scalato dall&#8217;imponibile irpef (di chi ha pagato) una quota di acquisto del prodotto (per ulteriori info su come funziona la detrazione fiscale Irpef <a title="Come funziona la detrazione fiscale Irpef - Wikipedia" href="http://it.wikipedia.org/wiki/Detrazione" target="_blank">leggi qui</a>).</p>
<p>In definitiva, &#8220;a conti fatti&#8221;, un videocitofono che costa 400 euro comprensivo di iva e installazione, <strong>lo pago la metà</strong> perchè detraggo dall&#8217;Irpef 200 euro in 10 anni.</p>
